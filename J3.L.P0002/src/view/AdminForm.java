    private void tblClicked(JTable tbl) {

        int selectedRow = tbl.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) tbl.getModel();
        
        String id = (String) model.getValueAt(selectedRow, 0);
        String title = (String) model.getValueAt(selectedRow, 1);
        String author = (String) model.getValueAt(selectedRow, 2);
        String category = (String) model.getValueAt(selectedRow, 3);
        String price = String.valueOf(model.getValueAt(selectedRow, 4));
        String quantity = String.valueOf(model.getValueAt(selectedRow, 5));
        String imgName = (String) model.getValueAt(selectedRow, 6);
        txtBookID.setEditable(false);
        txtBookID.setText(id);
        txtBookTitle.setText(title);
        txtAuthor.setText(author);
        setCateCbx(category);
        txtPrice.setText(price);
        txtQuantity.setText(quantity);
        txtImageName.setText(imgName);

    }

    private void setCateCbx(String str) {
        for (int i = 0; i < cbxCategories.getItemCount(); i++) {
            if (cbxCategories.getItemAt(i).equalsIgnoreCase(str)) {
                cbxCategories.setSelectedIndex(i);
            }
        }
    }


    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        int r = JOptionPane.showConfirmDialog(this, "Are you sure to Insert this book?", "Insert Book?", JOptionPane.YES_NO_OPTION);

        if (r == JOptionPane.YES_OPTION) {
            try {
                BooksDTO data = getDataInput("INSERT");
                boolean check = false;
                if (data != null) {
                    check = bookDAO.insertBook(data);
                }
                if (check) {
                    initData();
                    JOptionPane.showMessageDialog(this, "Successfully Inserted A Book!!!");
                }else{
                    JOptionPane.showMessageDialog(this, "Failed to Insert Book!!!");
                }

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Dupplicate Book ID!!!");
                Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnInsertActionPerformed

