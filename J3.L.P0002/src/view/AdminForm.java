    private void initData() {
        try {
            initTable(tblBooks);
            user = userDAO.getUserByID(id);
            lblUsername.setText(user.getUserName());

        } catch (SQLException ex) {
            Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void registerKeySearch() {
        currentTbl = tblBooks;
        txtSearch.addKeyListener(new KeyListener() {
            @Override
            public void keyTyped(KeyEvent e) {
                searchBook();
            }

            @Override
            public void keyPressed(KeyEvent e) {
                searchBook();
            }

            @Override
            public void keyReleased(KeyEvent e) {
                searchBook();
            }
        });
    }

    private void searchBook() {
        String search = txtSearch.getText();
        try {
            DefaultTableModel model = (DefaultTableModel) currentTbl.getModel();
            resetModel(model);
            Vector<BooksDTO> data = bookDAO.searchBook(search);
            data.forEach(book -> {
                model.addRow(book.toVector());
            });
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void resetModel(DefaultTableModel model) {
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
    }

    private void initTable(JTable tbl) throws SQLException, ClassNotFoundException {
        DefaultTableModel model = (DefaultTableModel) tbl.getModel();

        resetModel(model);
        Vector<BooksDTO> data = bookDAO.getActiveBooks();

        data.forEach(book -> {
            model.addRow(book.toVector());
        });
    }

